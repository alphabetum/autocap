##############################################################################
# Web maintenance tasks
##############################################################################

namespace :deploy do
  namespace :web do
    desc <<-DESC
      Present a maintenance page to visitors. Disables your application's web \
      interface by writing a "maintenance.html" file to each web server. The \
      servers must be configured to detect the presence of this file, and if \
      it is present, always display it instead of performing the request.

      By default, the maintenance page will just say the site is down for \
      "maintenance", and will be back "shortly", but you can customize the \
      page by specifying the REASON and UNTIL environment variables:

        $ cap deploy:web:disable \\
              REASON="hardware upgrade" \\
              UNTIL="12pm Central Time"

      Further customization will require that you write your own task.
    DESC
    task :disable, :roles => :web, :except => { :no_release => true } do
      require 'erb'
      on_rollback { run "rm #{current_path}/public/maintenance.html" }

      reason = ENV['REASON']
      deadline = ENV['UNTIL']
      rails_root = File.join(File.dirname(__FILE__), "..", "..", "..")
      template = if File.exist? File.join(File.dirname(__FILE__), rails_root, "app", "views", "maintenance", "maintenance.html.erb")
        File.read File.join(File.dirname(__FILE__), rails_root, "app", "views", "maintenance", "maintenance.html.erb")
      else
        File.read File.join(File.dirname(__FILE__), "..", "templates", "maintenance.html.erb")
      end
      result = ERB.new(template).result(binding)
      
      put result, "#{current_path}/public/maintenance.html", :mode => 0644
    end

    desc <<-DESC
      Makes the application web-accessible again. Removes the \
      "maintenance.html" page generated by deploy:web:disable, which (if your \
      web servers are configured correctly) will make your application \
      web-accessible again.
    DESC
    task :enable, :roles => :web, :except => { :no_release => true } do
      run "rm #{current_path}/public/maintenance.html"
    end
  end
end